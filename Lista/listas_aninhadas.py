# Listas podem armazenar outras listas
# Podemos criar estruturas bidimensionais  como tabelas
# Acessar elementos informando os indices de linha e coluna 
# Dá pra fazer representação de  Matriz

matriz=[     # <- abrimos uma lista que tem três listas dentro dela
     [1,"a",2], # linha 0
     ["b",3,4], # linha 1
     [6,5,"c"]  # linha 2
]

print(matriz[0])
print(matriz[0] [0])
print(matriz[0] [-1])
print(matriz[-1] [-1])
print(matriz[1])
print(matriz[2])
print(matriz[1] [-2])
print(matriz[2] [-3])
print(matriz[2] [0])
print(matriz[2] [-2])

